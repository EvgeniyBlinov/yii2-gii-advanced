<?php
/**
 * This is the template for generating the model class of a specified table.
 */

/* @var $this yii\web\View */
/* @var $generator yii\gii\generators\model\Generator */
/* @var $tableName string full table name */
/* @var $className string class name */
/* @var $tableSchema yii\db\TableSchema */
/* @var $labels string[] list of attribute labels (name => label) */
/* @var $rules string[] list of validation rules */
/* @var $relations array list of relations (name => relation declaration) */

echo "<?php\n";
?>

namespace <?= $generator->ns . '\\' . lcfirst($className) ?>;

use Yii;

<?php
    $baseClassDir = pathinfo($generator->baseClass, PATHINFO_DIRNAME);
    if ($baseClassDir != '.'): ?>
use <?=$generator->baseClass . ";\n"?>
<?php endif; ?>
<?php foreach ($relations as $name => $relation): ?>
<?php if ($relation[1] != $className):?>
use <?= $generator->ns . '\\' . lcfirst($relation[1]) . '\\' . ucfirst($relation[1]) . ";\n"?>
<?php endif;?>
<?php endforeach; ?>

/**
 * This is the BASE model class for table "<?= $generator->generateTableName($tableName) ?>".
 *
<?php foreach ($tableSchema->columns as $column): ?>
 * @property <?= "{$column->phpType} \${$column->name}\n" ?>
<?php endforeach; ?>
<?php if (!empty($relations)): ?>
 *
<?php foreach ($relations as $name => $relation): ?>
 * @property <?= $relation[1] . ($relation[2] ? '[]' : '') . ' $' . lcfirst($name) . "\n" ?>
<?php endforeach; ?>
<?php endif; ?>
 */
class <?= $className ?>Base extends <?=  pathinfo($generator->baseClass, PATHINFO_FILENAME) . "\n" ?>
{
    /**
     * @inheritdoc
     */
    public static function tableName()
    {
        return '<?= $generator->generateTableName($tableName) ?>';
    }
<?php if ($generator->db !== 'db'): ?>

    /**
     * @return \yii\db\Connection the database connection used by this AR class.
     */
    public static function getDb()
    {
        return Yii::$app->get('<?= $generator->db ?>');
    }
<?php endif; ?>

    /**
     * @inheritdoc
     */
    public function rules()
    {
        return [<?= "\n            " . implode(",\n            ", $rules) . "\n        " ?>];
    }

    /**
     * @inheritdoc
     */
    public function attributeLabels()
    {
        return [
<?php foreach ($labels as $name => $label): ?>
            <?php echo sprintf("'%s' => %s",
                $name,
                (($generator->useTranslations) ?
                    "Yii::t('app', " . $generator->generateString($label) . "),\n" :
                    $generator->generateString($label) . ",\n")) ?>
<?php endforeach; ?>
        ];
    }
<?php foreach ($relations as $name => $relation): ?>

    /**
     * @return \yii\db\ActiveQuery
     */
    public function get<?= $name ?>()
    {
        <?= $relation[0] . "\n" ?>
    }
<?php endforeach; ?>
}
